<template>
  <div class="records">
    <div class="records-wrapper">
      <Record
        v-for="(value, key, index) in records"
        :key="index"
        :recordTitle="key"
        :recordNumber="value"
      />
    </div>
  </div>
</template>

<script>
import Record from "./Record";
import { mapGetters } from "vuex";

export default {
  name: "AppFooter",
  components: { Record },
  computed: mapGetters({ getRecord: "todos/getRecord" }),
  data() {
    return {
      records: {}
    };
  },
  created: function() {
    this.records = this.getRecord;
  }
};
</script>

